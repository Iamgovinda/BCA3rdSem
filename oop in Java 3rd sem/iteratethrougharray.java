public class iteratethrougharray {
    public static void main(String[] args)
    {
        int[] arr={1,2,3,4,5};

        for(int number: arr)
        {
            System.out.println(number);
        }

        for(int i=0;i<=4;i++)
        {
            System.out.println(arr[i]);
        }
    }
}
